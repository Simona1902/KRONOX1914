package type;

/**
 * L'enumerazione IndizioCriptex definisce i diversi indizi (lettere) necessari
 * per risolvere l'enigma del Criptex nel gioco.
 * @author simona
 */
public enum IndizioCriptex {

    /**
     * Rappresenta l'indizio 'P'.
     */
    P,

    /**
     * Rappresenta l'indizio 'A'.
     */
    A,

    /**
     * Rappresenta l'indizio 'X'.
     */
    X
}
